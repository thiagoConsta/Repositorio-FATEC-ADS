class Calculadora {

  public void soma(float n1, float n2){
    float res = n1 + n2;
    System.out.println(res);
  }

  public void sub( float n1, float n2){
    float res = n1 - n2;
    System.out.println(res);
  }

  public void mult(float n1, float n2){
    float res = n1 * n2;
    System.out.println(res);
  }

  public void div(float n1, float n2){
    float res = n1 / n2;
    System.out.println(res);
  }
}
